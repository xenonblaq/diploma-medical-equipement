import pandas as pd             
import numpy as np               
import requests                  
from bs4 import BeautifulSoup   


class Armenia:
    """
    Парсер торговых данных Армении с сайта armstat.am.

    Идея:
    - ходим по страницам (пагинация через offset)
    - на каждой странице берем вторую таблицу, читаем ее в pandas
    - преобразуем широкую таблицу в длинную (экспорт/импорт -> строки)
    - приводим к единой схеме полей (метод decor)
    """

    def __init__(self, years=["2025"], belarus=False):
        # years: список лет (строки), которые нужно оставить в финальном датасете
        self.years = years

        # belarus:
        #   False -> тянем торговлю Армения-Россия (thid[]=643)
        #   True  -> тянем торговлю Армения-Беларусь (thid[]=112)
        self.belarus = belarus

    def decor(self, data):
        """
        Приводит сырые данные к стандартной схеме колонок проекта.
        На вход ожидается DataFrame со столбцами: year, month, ttype, value, ...
        """

        # Формируем "Отчетный период" в формате dd.mm.YYYY
        # Здесь всегда ставим день "01", месяц берём из data["month"], год из data["year"]
        data['Отчетный период'] = "01." + data["month"].astype(str).str.zfill(2) + '.' + data["year"].astype(str)

        # Фиксируем партнера в зависимости от режима
        if self.belarus:
            data["Страна-партнер"] = "Беларусь"
        else:
            data["Страна-партнер"] = "Россия"

        # Исходная страна всегда Армения
        data["Исходная страна"] = "Армения"

        # В этом парсере HS-коды не собираются, поэтому заполняются NaN
        data["Код товара (10 знаков)"] = np.nan
        data["Код товара (8 знаков)"] = np.nan
        data["Код товара (6 знаков)"] = np.nan
        data["Код товара (4 знака)"] = np.nan
        data["Код товара (2 знака)"] = np.nan

        # Стоимость берём из value (позже в parse умножаем на 1000)
        data["Значение (стоимость)"] = data["value"]

        # Массы/объемы/ДЭИ в источнике нет — оставляем NaN
        data["Значение (масса)"] = np.nan
        data["Единица объема"] = np.nan

        # Валюта фиксирована
        data["Единицы стоимости"] = "USD"

        # Доп. поля ДЭИ отсутствуют
        data["ДЭИ, описание"] = np.nan
        data["Дополнительная единица измерения (ДЭИ)"] = np.nan

        # Приводим направления к единому написанию
        # В источнике:
        #   "экспорт"
        #   "импорт (по стране отправления)"
        # В результате:
        #   "Экспорт"
        #   "Импорт"
        data["Направление"] = data["ttype"].replace({
            "экспорт": "Экспорт",
            "импорт (по стране отправления)": "Импорт"
        })

        # Эта строка ничего не делает (просто обращение к столбцу).
        # Можно удалить без последствий.
        data["Направление"]

        # Отбрасываем нулевые значения (и числом 0, и строкой '0')
        data = data[(data["Значение (стоимость)"] != 0) & (data["Значение (стоимость)"] != '0')]

        # Отбрасываем пропуски стоимости
        data = data[~data["Значение (стоимость)"].isna()]

        # Сортировка:
        # - сначала по году (убывание)
        # - потом по месяцу (убывание)
        # - потом по направлению (Экспорт/Импорт) по алфавиту
        # key=... нужен, чтобы year/month сортировались как числа, а не строки
        data = data.sort_values(
            by=['year', 'month', 'Направление'],
            ascending=[False, False, True],
            key=lambda col: col.astype(int) if col.name in ['year', 'month'] else col,
            ignore_index=True
        )

        # Оставляем только стандартный набор колонок в правильном порядке
        data = data[[
            "Отчетный период", "Исходная страна", "Страна-партнер", "Направление",
            "Код товара (2 знака)", "Код товара (4 знака)", "Код товара (6 знаков)",
            "Код товара (8 знаков)", "Код товара (10 знаков)", "Значение (стоимость)",
            "Единицы стоимости", "Значение (масса)", "Единица объема",
            "Дополнительная единица измерения (ДЭИ)", "ДЭИ, описание"
        ]]

        # Поле на будущее (стоимость, выраженная в ДЭИ) — здесь нет данных
        data["Значение (стоимость) - ДЭИ"] = np.nan

        # Фильтруем по годам, которые передали в self.years
        # Берем последние 4 символа из "Отчетный период" (год) и проверяем вхождение
        data = data[data["Отчетный период"].str[-4:].isin(self.years)]

        # Приводим "Отчетный период" к datetime
        data["Отчетный период"] = pd.to_datetime(data["Отчетный период"], format="%d.%m.%Y")

        return data

    def parse(self) -> pd.DataFrame:
        """
        Основной метод парсинга.
        Возвращает DataFrame в стандартизированном формате (через decor()).
        """

        dfs = []     # сюда собираем результаты со всех страниц
        page = 0     # offset в запросе (пагинация)

        while True:
            # Формируем URL.
            # Важно:
            # - thid[] определяет партнера: 112 (Беларусь) или 643 (Россия)
            # - year[]=... и monid[]=... захардкожены в URL (2019-2025 и 1-12)
            # - offset={page} управляет страницей результатов
            if self.belarus:
                response = requests.get(
                    url=(
                        "https://armstat.am/ru/index.php?nid=717&thid%5B%5D=112"
                        "&year%5B%5D=2025&year%5B%5D=2024&year%5B%5D=2023&year%5B%5D=2022"
                        "&year%5B%5D=2021&year%5B%5D=2020&year%5B%5D=2019"
                        "&monid%5B%5D=1&monid%5B%5D=2&monid%5B%5D=3&monid%5B%5D=4"
                        "&monid%5B%5D=5&monid%5B%5D=6&monid%5B%5D=7&monid%5B%5D=8"
                        "&monid%5B%5D=9&monid%5B%5D=10&monid%5B%5D=11&monid%5B%5D=12"
                        "&submit=%D0%9F%D0%BE%D0%B8%D1%81%D0%BA"
                        f"&offset={page}"
                    )
                ).text
            else:
                response = requests.get(
                    url=(
                        "https://armstat.am/ru/index.php?nid=717&thid%5B%5D=643"
                        "&year%5B%5D=2025&year%5B%5D=2024&year%5B%5D=2023&year%5B%5D=2022"
                        "&year%5B%5D=2021&year%5B%5D=2020&year%5B%5D=2019"
                        "&monid%5B%5D=1&monid%5B%5D=2&monid%5B%5D=3&monid%5B%5D=4"
                        "&monid%5B%5D=5&monid%5B%5D=6&monid%5B%5D=7&monid%5B%5D=8"
                        "&monid%5B%5D=9&monid%5B%5D=10&monid%5B%5D=11&monid%5B%5D=12"
                        "&submit=%D0%9F%D0%BE%D0%B8%D1%81%D0%BA"
                        f"&offset={page}"
                    )
                ).text

            # Достаём вторую таблицу на странице:
            # BeautifulSoup(...).find_all("table")[1]
            # Если второй таблицы нет -> IndexError -> считаем, что страницы закончились -> break
            try:
                table = str(BeautifulSoup(response, "lxml").find_all(name="table")[1])
            except IndexError:
                break

            # Читаем HTML-таблицу в pandas.
            # header=0 означает, что первая строка таблицы — это заголовки столбцов.
            df = pd.read_html(table, header=0)[0]

            # Превращаем таблицу "широкого" вида:
            #   страна | год | Временной Период | экспорт | импорт ...
            # в "длинный" вид, где экспорт/импорт становятся строками:
            melted = df.melt(
                id_vars=['страна', 'год', 'Временной Период'],
                value_vars=['экспорт', 'импорт (по стране отправления)'],
                var_name='ttype',
                value_name='value'
            )

            # Добавляем константные колонки
            melted["country"] = "Армения"
            melted["unit"] = "USD"

            # ВАЖНО: здесь value умножается на 1000
            # (значит в исходной таблице, вероятно, "тыс. USD")
            melted["value"] = melted["value"].astype(float) * 1000

            # Приводим период (месяц) к формату "01".."12"
            melted["Временной Период"] = melted["Временной Период"].astype(str).str.zfill(2)

            # Переименовываем колонки на английские/стандартные
            melted = melted.rename(columns={
                "страна": "country_1",
                "год": "year",
                "Временной Период": "month"
            })

            # Складываем только нужные поля в общий список dfs
            dfs.append(melted[["year", "month", "country", "country_1", "ttype", "value", "unit"]])

            # Следующая страница (следующий offset)
            page += 1

        # Склеиваем всё в один DataFrame
        df = pd.concat(dfs)

        # Приводим к стандартному формату проекта
        return self.decor(df)
